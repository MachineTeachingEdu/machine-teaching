def season(month):
    if month == 1 or month == 2 or month == 12:
        return "summer"
    elif month == 3 or month == 4 or month == 5:
        return "autumn"
    elif month == 6 or month == 7 or month == 8:
        return "winter"
    elif month == 9 or month == 10 or month == 11:
        return "spring"
